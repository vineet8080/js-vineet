let month="jan"

switch (month) {
    case "jan":
        console.log("jan")
        break;
    case "Feb":
        console.log("feb");
        break;
    case "march":
        console.log("march");
        break;
    case "april":
        console.log("april");
        break; 

    default:
        console.log("invalid data ");
        
        break;
}


